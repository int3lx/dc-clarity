window.dclarityGlobal = window.dclarityGlobal || {
    equipmentTypes: [
        { value: "GENSET", label: "GENSET System" },
        { value: "UPS", label: "UPS & Battery System" },
        { value: "CRAC", label: "CRAC System" },
        { value: "CHILLER", label: "Chiller System" },
        { value: "FIRE", label: "Fire Suppression System" }
    ],

    dcLocations: [
        { value: "BFDC", label: "BFDC" },
        { value: "KJDC", label: "KJDC" },
        { value: "KVDC", label: "KVDC" },
        { value: "SJDC", label: "SJDC" },
        { value: "KTEDC", label: "KTEDC" },
        { value: "IPEDC", label: "IPEDC" },
        { value: "KNEDC", label: "KNEDC" },
        { value: "SNEDC", label: "SNEDC" },
        { value: "KGEDC", label: "KGEDC" },
        { value: "KKEDC", label: "KKEDC" }
    ],

    populateSelect: function(selectId, items, placeholder) {
        const select = document.getElementById(selectId);
        if (!select || !Array.isArray(items)) {
            return;
        }

        select.innerHTML = '<option value="">' + placeholder + '</option>';
        items.forEach(function(item) {
            const option = document.createElement("option");
            option.value = item.value;
            option.textContent = item.label;
            select.appendChild(option);
        });
    },

    populateEquipmentTypeDropdown: function() {
        this.populateSelect("dcfm_equipment_type", this.equipmentTypes, "Select Equipment Type");
    },

    populateDCLocationDropdown: function() {
        this.populateSelect("dc_location", this.dcLocations, "Select Location");
    },

    getElementValue: function(id) {
        const element = document.getElementById(id);
        if (!element) {
            return "";
        }

        if (element.tagName === "SELECT") {
            const option = element.options[element.selectedIndex];
            return option ? option.text : element.value;
        }

        return element.value || "";
    },

    getAutoGeneratedValue: function() {
        const config = this.autoGenerateConfig;
        const values = {};

        config.sourceIds.forEach(function(id) {
            values[id] = window.dclarityGlobal.getElementValue(id);
        });

        let generated = config.format || "";
        Object.keys(values).forEach(function(id) {
            const placeholder = "{" + id + "}";
            generated = generated.split(placeholder).join(values[id] || "");
        });

        if (!generated) {
            generated = config.sourceIds.map(function(id) {
                return values[id];
            }).filter(Boolean).join(" ");
        }

        return generated;
    },

    bindAutoGenerate: function() {
        this.unbindAutoGenerate();

        const config = this.autoGenerateConfig;
        const target = document.getElementById(config.targetId);
        if (!target || !Array.isArray(config.listenerIds)) {
            return;
        }

        const update = function() {
            target.value = window.dclarityGlobal.getAutoGeneratedValue();
        };

        this.autoGenerateListeners = [];
        config.listenerIds.forEach(function(id) {
            const element = document.getElementById(id);
            if (!element) {
                return;
            }

            element.addEventListener("change", update);
            element.addEventListener("input", update);
            window.dclarityGlobal.autoGenerateListeners.push({
                element: element,
                handler: update
            });
        });

        update();
    },

    unbindAutoGenerate: function() {
        if (!Array.isArray(this.autoGenerateListeners)) {
            return;
        }

        this.autoGenerateListeners.forEach(function(binding) {
            binding.element.removeEventListener("change", binding.handler);
            binding.element.removeEventListener("input", binding.handler);
        });

        this.autoGenerateListeners = [];
    }
};

if (typeof window !== "undefined") {
    window.addEventListener("DOMContentLoaded", function() {
        if (window.dclarityGlobal) {
            if (typeof window.dclarityGlobal.populateEquipmentTypeDropdown === "function") {
                window.dclarityGlobal.populateEquipmentTypeDropdown();
            }
            if (typeof window.dclarityGlobal.populateDCLocationDropdown === "function") {
                window.dclarityGlobal.populateDCLocationDropdown();
            }
            if (typeof window.dclarityGlobal.bindAutoGenerate === "function") {
                window.dclarityGlobal.bindAutoGenerate();
            }
        }
    });
}
